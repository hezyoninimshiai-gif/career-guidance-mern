const mongoose = require("mongoose");

// User Schema (Blueprint)
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },

  email: {
    type: String,
    required: true,
    unique: true,
  },

  password: {
    type: String,
    required: true,
  },

  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Create Model
const User = mongoose.model("User", userSchema);

module.exports = User;